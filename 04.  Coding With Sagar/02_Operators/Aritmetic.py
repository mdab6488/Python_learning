# for math calculation
"""
| Operator | Name           | Description                               | Example  | Result |
| -------- | -------------- | ----------------------------------------- | -------- | ------ |
| `+`      | Addition       | Adds two numbers                          | `5 + 3`  | `8`    |
| `-`      | Subtraction    | Subtracts right operand from left         | `10 - 4` | `6`    |
| `*`      | Multiplication | Multiplies two numbers                    | `2 * 6`  | `12`   |
| `/`      | Division       | Divides left by right, returns float      | `9 / 2`  | `4.5`  |
| `//`     | Floor Division | Divides and removes decimal part (floors) | `9 // 2` | `4`    |
| `%`      | Modulus        | Returns remainder of division             | `9 % 2`  | `1`    |
| `**`     | Exponentiation | Raises left to the power of right         | `2 ** 3` | `8`    |
"""

x = 10
y = 20

print(x+y) # 30
print(x-y) # -10
print(x*y) # 200
print(x/y) # 0.5
print(x//y) # 0
print(x%y) # 10
print(x**y) # 100000000000000000000
